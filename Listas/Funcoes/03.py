# Faça uma função para cada operação matemática básica (soma, subtração, multiplicação e divisão). 
# As funções devem receber dois números e retornar o resultado da operação.

num_1 = 10
num_2 = 1

def soma(num_1, num_2):
    soma = num_1 + num_2
    return soma

def sub(num_1, num_2):
    sub = num_1 - num_2
    return sub

def multi(num_1, num_2):
    multi = num_1 * num_2
    return multi

def divi(num_1, num_2):
    divi = num_1 / num_2
    return divi

print(soma(num_1, num_2))
print(sub(num_1, num_2))
print(multi(num_1, num_2))
print(divi(num_1, num_2))
